    private void btnIniciarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnIniciarMouseClicked
        errorMensaje.setVisible(false);
        if(jComboBox1.getSelectedItem().toString().equals("Seleccione un usuario")){
            errorMensaje.setVisible(true);
            errorMensaje.setText("Seleccione un usuario");
            return;
        }
        daos.setSelectedStaff(jComboBox1.getSelectedItem().toString());
        CRUD_interfaz interfaz = new CRUD_interfaz(daos);
        interfaz.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnIniciarMouseClicked

