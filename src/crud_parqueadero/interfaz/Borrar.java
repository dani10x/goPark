    private void seleccionar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_seleccionar1MouseClicked

        errorMensaje.setVisible(false);
        finalizadoMensaje.setVisible(false);
        String capNumber=t_number1.getText();
        if(capNumber.equals("Ingrese el numero") || capNumber.length()>10){
            errorMensaje.setVisible(true);
            errorMensaje.setText("Numero no valido");
            return;
        }
        try {
            tickets buscar = daos.buscar(capNumber);
            if(buscar.getDeparture_time()==null){
                errorMensaje.setVisible(true);
                errorMensaje.setText("El ticket debe ser cobrado primero");
                return;
            }
            if(buscar.getCLIENT_Id()==null){
                errorMensaje.setVisible(true);
                errorMensaje.setText("Ticket no valido");
                jLabel16.setVisible(false);
                si.setVisible(false);
                no.setVisible(false);
                btnSi.setVisible(false);
                btnNo.setVisible(false);
                finalizadoMensaje.setVisible(false);
                return;
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Borrar.class.getName()).log(Level.SEVERE, null, ex);
        }
        jLabel16.setVisible(true);
        si.setVisible(true);
        no.setVisible(true);
        btnSi.setVisible(true);
        btnNo.setVisible(true);
        capId=capNumber;
    }//GEN-LAST:event_seleccionar1MouseClicked

