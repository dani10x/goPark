    private void btnCrearMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCrearMousePressed
        errorMensaje.setVisible(false);
        finalizadoMensaje.setVisible(false);
        String capId= id.getText();
        if(capId.equals("Ingrese la identificacion") || capId.length()!=10 || !capId.matches("^[1-9]{1}[0-9]{9}$")){
            errorMensaje.setText("Identificacion no valida");
            errorMensaje.setVisible(true);
            return;
        }
        String capNombre = name.getText();
        if(capNombre.equals("Ingrese el nombre") || capNombre.length()>12){
            errorMensaje.setText("Nombre no valido");
            errorMensaje.setVisible(true);
            return;
        }
        String capTelefono = cellphone.getText();
        if(capTelefono.equals("Ingrese el numero de telefono") || capTelefono.length()!=10 || !capTelefono.matches("^[1-9]{1}[0-9]{9}$")){
            errorMensaje.setText("Numero de telefono no valido");
            errorMensaje.setVisible(true);
            return;
        }
        daos.crearCliente(capId, capNombre, capTelefono);
        finalizadoMensaje.setVisible(true);
        id.setText("Ingrese la identificacion");
        id.setForeground(new Color(153, 153, 153));
        name.setText("Ingrese el nombre");
        name.setForeground(new Color(153, 153, 153));
        cellphone.setText("Ingrese el numero de telefono");
        cellphone.setForeground(new Color(153, 153, 153));
    }//GEN-LAST:event_btnCrearMousePressed

